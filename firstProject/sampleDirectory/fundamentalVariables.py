# Integer
x = 1000
print(x)
print(id(x))
print(type(x))

x = 2000
print(x)
print(id(x))
print(type(x))

z = 2000
print(z)
print(id(z))
print(type(z))

# List
x = [1000]
print(x)
print(id(x))
print(type(x))

z = [1000]
print(z)
print(id(z))
print(type(z))

y = x
print(y)
print(id(y))
print(type(y))

# Complex
a = 3 + 4j
print(a)
print(id(a))
print(type(a))

# Boolean
b = True
print(b)
print(id(b))
print(type(b))
